    /**
     * Manipulates the map when it's available.
     * This callback is triggered when the map is ready to be used.
     */
    @Override
    public void onMapReady(GoogleMap map) {
        this.map = map;

        if (selectedLocation != null){
            map.addMarker(new MarkerOptions()
                    .position(new LatLng(selectedLocation.getLatitude(), selectedLocation.getLongitude()))
                    .draggable(true));
        }

        // Use a custom info window adapter to handle multiple lines of text in the
        // info window contents.
        this.map.setInfoWindowAdapter(new GoogleMap.InfoWindowAdapter() {

            @Override
            // Return null here, so that getInfoContents() is called next.
            public View getInfoWindow(Marker arg0) {
                return null;
            }

            @Override
            public View getInfoContents(Marker marker) {
                // Inflate the layouts for the info window, title and snippet.
                View infoWindow = getLayoutInflater().inflate(R.layout.custom_info_contents,
                        (FrameLayout) findViewById(R.id.map), false);

                TextView title = infoWindow.findViewById(R.id.title);
                title.setText(marker.getTitle());

                TextView snippet = infoWindow.findViewById(R.id.snippet);
                snippet.setText(marker.getSnippet());

                return infoWindow;
            }
        });

        // Prompt the user for permission.
        getLocationPermission();

        // Turn on the My Location layer and the related control on the map.
        updateLocationUI();

        // Get the current location of the device and set the position of the map.
        getDeviceLocation();

        Toast.makeText(getApplicationContext(), "Click on the map to select location.", Toast.LENGTH_LONG).show();

        map.setOnMapClickListener(new GoogleMap.OnMapClickListener() {

            int numberOfMarkers = 0;

            @Override
            public void onMapClick(LatLng point) {
                map.clear();

                Toast.makeText(getApplicationContext(), "Location selected.", Toast.LENGTH_LONG).show();

                Location mLocation = new Location(String.valueOf(point));


                    MarkerOptions marker = new MarkerOptions().position(point).draggable(true);
                    map.addMarker(marker);
                    numberOfMarkers++;
                    selectedLocation = new Location(String.valueOf(point));
                    selectedLocation.setLongitude(point.longitude);
                    selectedLocation.setLatitude(point.latitude);

                map.setOnMarkerDragListener(new GoogleMap.OnMarkerDragListener() {

                    @Override
                    public void onMarkerDrag(Marker marker) {

                    }
                    @Override
                    public void onMarkerDragEnd(Marker marker) {
                        LatLng newLocation = marker.getPosition();
                        mLocation.setLatitude(newLocation.latitude);
                        mLocation.setLongitude(newLocation.longitude);
                        map.moveCamera(CameraUpdateFactory.newLatLngZoom(new LatLng(mLocation.getLatitude(), mLocation.getLongitude()), 15.0f));

                    }
                    @Override
                    public void onMarkerDragStart(Marker marker) {}

                });
            }
        });
    }

    /**
     * Gets the current location of the device, and positions the map's camera.
     */
    private void getDeviceLocation() {
        /*
         * Get the best and most recent location of the device, which may be null in rare
         * cases when a location is not available.
         */
        try {
            if (locationPermissionGranted) {
                Task<Location> locationResult = fusedLocationProviderClient.getLastLocation();
                locationResult.addOnCompleteListener(this, new OnCompleteListener<Location>() {
                    @Override
                    public void onComplete(@NonNull Task<Location> task) {
                        if (task.isSuccessful()) {
                            // Set the map's camera position to the current location of the device.
                            lastKnownLocation = task.getResult();
                            if (lastKnownLocation != null) {
                                map.moveCamera(CameraUpdateFactory.newLatLngZoom(
                                        new LatLng(lastKnownLocation.getLatitude(),
                                                lastKnownLocation.getLongitude()), DEFAULT_ZOOM));
                            }
                        } else {
                            Log.d(TAG, "Current location is null. Using defaults.");
                            Log.e(TAG, "Exception: %s", task.getException());
                            map.moveCamera(CameraUpdateFactory
                                    .newLatLngZoom(defaultLocation, DEFAULT_ZOOM));
                            map.getUiSettings().setMyLocationButtonEnabled(false);
                        }
                    }
                });
            }
        } catch (SecurityException e)  {
            Log.e("Exception: %s", e.getMessage(), e);
        }
    }

    /**
     * Prompts the user for permission to use the device location.
     */
    private void getLocationPermission() {
        /*
         * Request location permission, so that we can get the location of the
         * device. The result of the permission request is handled by a callback,
         * onRequestPermissionsResult.
         */
        if (ContextCompat.checkSelfPermission(this.getApplicationContext(),
                android.Manifest.permission.ACCESS_FINE_LOCATION)
                == PackageManager.PERMISSION_GRANTED) {
            locationPermissionGranted = true;
        } else {
            ActivityCompat.requestPermissions(this,
                    new String[]{android.Manifest.permission.ACCESS_FINE_LOCATION},
                    PERMISSIONS_REQUEST_ACCESS_FINE_LOCATION);
        }
    }

    /**
     * Handles the result of the request for location permissions.
     */
    @Override
    public void onRequestPermissionsResult(int requestCode,
                                           @NonNull String[] permissions,
                                           @NonNull int[] grantResults) {
        locationPermissionGranted = false;
        if (requestCode
                == PERMISSIONS_REQUEST_ACCESS_FINE_LOCATION) {// If request is cancelled, the result arrays are empty.
            if (grantResults.length > 0
                    && grantResults[0] == PackageManager.PERMISSION_GRANTED) {
                locationPermissionGranted = true;
            }
        } else {
            super.onRequestPermissionsResult(requestCode, permissions, grantResults);
        }
        updateLocationUI();
    }


    /**
     * Gets the location selected by the user
     */
    private void getPlace(Location location) {

        //Intent intent = new Intent(MapsActivityCurrentPlace.this, ReportActivity.class);
        String address = getAddress(MapsActivityCurrentPlace.this,location);
        // intent.putExtra("location", address);
        // startActivity(intent);
        setResult(Activity.RESULT_OK,
                new Intent().putExtra("location", address));
        finish();
    }

    /**
     * Updates the map's UI settings based on whether the user has granted location permission.
     */
    private void updateLocationUI() {
        if (map == null) {
            return;
        }
        try {
            if (locationPermissionGranted) {
                map.setMyLocationEnabled(true);
                map.getUiSettings().setMyLocationButtonEnabled(true);
            } else {
                map.setMyLocationEnabled(false);
                map.getUiSettings().setMyLocationButtonEnabled(false);
                lastKnownLocation = null;
                getLocationPermission();
            }
        } catch (SecurityException e)  {
            Log.e("Exception: %s", e.getMessage());
        }
    }

    public String getAddress(Context context, Location location) {
        double lat = location.getLatitude();
        double lng = location.getLongitude();
        Geocoder geocoder = new Geocoder(context, Locale.getDefault());
        try {
            List<Address> addresses = geocoder.getFromLocation(lat, lng, 1);
            Address obj = addresses.get(0);

            String locationString = "";

            if (obj.getAddressLine(0) != null)
                locationString += obj.getAddressLine(0);
            else {
                if (obj.getCountryName() != null)
                    locationString += obj.getCountryName();
                if (obj.getCountryName() != null)
                    locationString += obj.getAdminArea();
                if (obj.getPostalCode() != null)
                    locationString += obj.getPostalCode();
                if (obj.getSubAdminArea() != null)
                    locationString += obj.getSubAdminArea();
                if (obj.getLocality() != null)
                    locationString += obj.getLocality();
                if (obj.getSubThoroughfare() != null)
                    locationString += obj.getSubThoroughfare();
            }
            return locationString;
        } catch (IOException e) {
            e.printStackTrace();
            Toast.makeText(this, e.getMessage(), Toast.LENGTH_SHORT).show();
            return null;
        }
    }
